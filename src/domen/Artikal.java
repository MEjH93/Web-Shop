package domen;

public class Artikal {
	private int idArtikla;
	private String naziv;
	private double cena;
	private String imeSlike;
	private int idKategorije;
	public int getIdArtikla() {
		return idArtikla;
	}
	public void setIdArtikla(int idArtikla) {
		this.idArtikla = idArtikla;
	}
	public String getNaziv() {
		return naziv;
	}
	public void setNaziv(String naziv) {
		this.naziv = naziv;
	}
	public double getCena() {
		return cena;
	}
	public void setCena(double cena) {
		this.cena = cena;
	}
	public String getImeSlike() {
		return imeSlike;
	}
	public void setImeSlike(String imeSlike) {
		this.imeSlike = imeSlike;
	}
	public int getIdKategorije() {
		return idKategorije;
	}
	public void setIdKategorije(int idKategorije) {
		this.idKategorije = idKategorije;
	}
	

}
